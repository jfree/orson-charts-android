package com.orsoncharts.android.util;

/**
 * Scaling options for fitting rectangles.
 */
public enum Scale2D {
    
    /** No scaling. */
    NONE, 
    
    /** Scale horizontally (but not vertically). */
    SCALE_HORIZONTAL,
    
    /** Scale vertically (but not horizontally). */
    SCALE_VERTICAL, 
    
    /** Scale both horizontally and vertically. */
    SCALE_BOTH;
    
}
