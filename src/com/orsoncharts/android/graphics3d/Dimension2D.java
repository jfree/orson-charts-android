package com.orsoncharts.android.graphics3d;

public class Dimension2D {

	private float width;
	
	private float height;
	
	public Dimension2D(float width, float height) {
		this.width = width;
		this.height = height;
	}
	
	public float getWidth() {
		return this.width;
	}
	
	public float getHeight() {
		return this.height;
	}
}
