package com.orsoncharts.android.table;

public class Insets {

	public float left;
	
	public float top;
	
	public float right;

	public float bottom;
	
	public Insets(float left, float top, float right, float bottom) {
		this.left = left;
		this.top = top;
		this.right = right;
		this.bottom = bottom;
	}
}
